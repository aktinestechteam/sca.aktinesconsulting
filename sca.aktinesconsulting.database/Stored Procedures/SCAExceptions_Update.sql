           ,@VolumeRangeTo DECIMAL(18,4)=NULL
           ,@IsActive BIT=1
           ,@UpdatedOn  DATETIME = NULL
		   ,@UpdatedBy INT = NULL
AS
BEGIN
UPDATE [dbo].[SCAExceptions] SET
	[StartDate]=@StartDate,
	[EndDate]=@EndDate,
	[Description]=@Description,
	[BookingOrigin]=@BookingOrigin,
	[BookingDestination]=@BookingDestination,
	[BookingRateType]=@BookingRateType,
	[AgentCode]=@AgentCode,
	[BookingDestinationCountry]=@BookingDestinationCountry,
	[BookingDestinationRegion]=@BookingDestinationRegion,
	[BookingOriginCountry]=@BookingOriginCountry,
	[BookingOriginSalesArea]=@BookingOriginSalesArea,
