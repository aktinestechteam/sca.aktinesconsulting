	[ExcludeBookingDestination]=@ExcludeBookingDestination,
	[ExcludeBookingRateType]=@ExcludeBookingRateType,
	[ExcludeBookingDestinationRegion]=@ExcludeBookingDestinationRegion,
	[ExcludeAgentCode]=@ExcludeAgentCode,
	[ExcludeBookingDestinationCountry]=@ExcludeBookingDestinationCountry,
	[ExcludeBookingOriginCountry]=@ExcludeBookingOriginCountry,
	[ExcludeBookingOriginSalesArea]=@ExcludeBookingOriginSalesArea,
	[ExcludeBookingOriginRegion]=@ExcludeBookingOriginRegion,
	[ExcludeChannel]=@ExcludeChannel,
	[ExcludeSpecialHandlingCodes]=@ExcludeSpecialHandlingCodes,
