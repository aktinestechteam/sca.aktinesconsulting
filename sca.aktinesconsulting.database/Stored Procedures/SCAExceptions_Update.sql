           ,@IsVolumeLessThan BIT=0
           ,@IsVolumeEqualTo BIT=0
           ,@IsVolumeLessthanEqualTo BIT=0
           ,@IsVolumeGreaterThan BIT=0
           ,@IsVolumeGreaterThanEqualTo BIT=0
